CREATE OR REPLACE PROCEDURE SP_READPLATO (VID IN NUMBER)
AS
    NOMBRE VARCHAR2(25);
    NOMBRET VARCHAR(25);
BEGIN
    SELECT P.NOMBRE, T.NOMBRE
    INTO NOMBRE, NOMBRET
    FROM TBPLATO P, TBTIPOPLATO T
    WHERE VID = P.IDPLATO
    AND P.IDPLATO = T.IDTIPOPLATO;
    
    DBMS_OUTPUT.PUT_LINE('NOMBRE: ' || NOMBRE || ' TIPO: ' || NOMBRET);
END;


