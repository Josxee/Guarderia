CREATE OR REPLACE VIEW VWautorizado AS SELECT 
    cedulaAutorizado, 
    nombreAutorizado, primerApellidoAutorizado, segundoApellidoAutorizado, 
    direccion, 
    telefono, 
    responsablePago
FROM
    TBautorizado;

CREATE OR REPLACE VIEW VWinformacionGeneralNino AS SELECT 
       n.cedulaNino, n.nombreNino, n.primerApellidoNino, n.segundoApellidoNino, n.fechaNacimiento, n.fechaIngreso, n.fechaBaja,
       a.cedulaAutorizado, a.nombreAutorizado, a.primerApellidoAutorizado, a.segundoApellidoAutorizado, a.direccion, a.telefono, a.responsablePago,
       r.relacion
FROM TBnino n
INNER JOIN TBrelacionAutorizacion r ON n.cedulaNino = r.cedulaNino
INNER JOIN TBautorizado a ON r.cedulaAutorizado = a.cedulaAutorizado
ORDER BY cedulaNino;


CREATE OR REPLACE VIEW VWinformacionAlergias AS SELECT 
       n.cedulaNino, n.nombreNino, n.primerApellidoNino, n.segundoApellidoNino,
       i.idIngrediente, i.nombre AS nombreIngrediente,
       t.idTipoIngrediente, t.nombre AS nombreTipoIngrediente
FROM TBnino n
LEFT JOIN TBalergia a ON n.cedulaNino = a.cedulaNino
LEFT JOIN TBingrediente i ON a.idIngrediente = i.idIngrediente
LEFT JOIN TBtipoIngrediente t ON i.idTipoIngrediente = t.idTipoIngrediente;


CREATE OR REPLACE VIEW VWinformacionIngredientes AS SELECT 
        i.nombre AS nombreIngrediente, t.nombre AS nombreTipoIngrediente
FROM TBingrediente i
LEFT JOIN TBtipoIngrediente t ON i.idTipoIngrediente = t.idTipoIngrediente;


CREATE OR REPLACE VIEW VWinformacionEncargadosPagos AS SELECT 
    p.cedulaAutorizado, a.nombreAutorizado, a.primerApellidoAutorizado, a.segundoApellidoAutorizado, a.telefono, p.montoMensualFijo
FROM TBpago p
INNER JOIN TBautorizado a ON p.cedulaAutorizado = a.cedulaAutorizado;


CREATE OR REPLACE VIEW VWinformacionPlatos AS SELECT 
        tp.idTipoPlato, tp.nombre AS nombreTipoPlato, p.idPlato, p.nombre AS nombrePlato
FROM TBtipoPlato tp
INNER JOIN TBplato p ON tp.idTipoPlato = p.idTipoPlato;


CREATE OR REPLACE VIEW VWinformacionIngredientes AS SELECT 
    ti.idTipoIngrediente, ti.nombre AS nombreTipoIngrediente, i.idIngrediente, i.nombre AS nombreIngrediente
FROM TBtipoIngrediente ti
INNER JOIN TBingrediente i ON ti.idTipoIngrediente = i.idTipoIngrediente;


CREATE OR REPLACE VIEW VWinformacionMenu AS SELECT 
     mp.idMenuPlato, m.idMenu, m.nombre AS nombreMenu, p.idPlato, p.nombre AS nombrePlato
FROM TBmenuPlato mp
INNER JOIN TBmenu m ON mp.idMenu = m.idMenu
INNER JOIN TBplato p ON mp.idPlato = p.idPlato
ORDER BY idMenu;


CREATE OR REPLACE VIEW VWinformacionPlatoIngredientes AS SELECT 
    pi.idPlatoIngrediente, p.idPlato, p.nombre AS nombrePlato, i.idIngrediente, i.nombre AS nombreIngrediente
FROM TBplatoIngrediente pi
JOIN TBplato p ON pi.idPlato = p.idPlato
JOIN TBingrediente i ON pi.idIngrediente = i.idIngrediente
ORDER BY idPlato;




