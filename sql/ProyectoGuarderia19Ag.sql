PROCEDURE SP_READRELACIONAUT (
    VID IN NUMBER,
    VCURSOR OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN VCURSOR FOR
        SELECT N.NOMBRENINO || ' ' || N.PRIMERAPELLIDONINO || ' ' || N.SEGUNDOAPELLIDONINO AS VNINO,
               A.NOMBREAUTORIZADO || ' ' || A.PRIMERAPELLIDOAUTORIZADO || ' ' || A.SEGUNDOAPELLIDOAUTORIZADO AS VAUT,
               RA.RELACION AS VRELACION
        FROM TBNINO N
        JOIN TBRELACIONAUTORIZACION RA ON RA.CEDULANINO = N.CEDULANINO
        JOIN TBAUTORIZADO A ON RA.CEDULAAUTORIZADO = A.CEDULAAUTORIZADO
        WHERE RA.CEDULANINO = VID;
END;
