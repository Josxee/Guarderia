CREATE OR REPLACE PROCEDURE SP_READPLATOINGREDIENTE(VID IN NUMBER)
AS
    VNOMBREPLATO VARCHAR2(100);
    VNOMBREINGRE VARCHAR2(100);

    CURSOR DATOS IS SELECT P.NOMBRE, I.NOMBRE
    FROM TBPLATOINGREDIENTE PI, TBPLATO P, TBINGREDIENTE I
    WHERE PI.IDPLATO = VID
    AND PI.IDPLATO = P.IDPLATO
    AND PI.IDINGREDIENTE = I.IDINGREDIENTE;
BEGIN
    OPEN DATOS;
        LOOP
            FETCH DATOS INTO VNOMBREPLATO, VNOMBREINGRE;
                DBMS_OUTPUT.PUT_LINE('PLATO: ' || VNOMBREPLATO || ' INGREDIENTE: ' || VNOMBREINGRE);
            EXIT WHEN DATOS%NOTFOUND;
        END LOOP;
END;

