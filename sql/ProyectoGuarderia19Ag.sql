CREATE OR REPLACE PROCEDURE SP_READPAGO (VID IN NUMBER)
AS
    AUTORIZADO VARCHAR2(100);
    MONTO NUMBER;
    VNUMEROCUENTA NUMBER;
BEGIN
    SELECT A.NOMBREAUTORIZADO || ' ' || A.PRIMERAPELLIDOAUTORIZADO, P.MONTOMENSUALFIJO, P.NUMEROCUENTA
    INTO AUTORIZADO, MONTO, VNUMEROCUENTA
    FROM TBPAGO P, TBAUTORIZADO A
    WHERE VID = P.IDPAGO
    AND P.CEDULAAUTORIZADO = A.CEDULAAUTORIZADO;
    
    DBMS_OUTPUT.PUT_LINE('NOMBRE: ' || AUTORIZADO || ' MONTO MENDUAL FIJO: ' || MONTO || ' NUMERO DE CUENTA: ' || VNUMEROCUENTA);
END;

