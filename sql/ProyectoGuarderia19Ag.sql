 RETURN NUMBER
 AS
 CANT NUMBER;
 BEGIN
     SELECT COUNT(*)
     INTO CANT 
     FROM TBconsumo
     WHERE cedulaNino = VCED AND EXTRACT(MONTH FROM fechaConsumo) = VMES;
