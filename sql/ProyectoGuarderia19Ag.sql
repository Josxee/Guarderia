);
-------------------------------------------------------

CREATE TABLE TBmenu (
idMenu NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY, 
nombre VARCHAR2(80) NOT NULL,
CONSTRAINT PKTBmenu PRIMARY KEY(idMenu)
);
-------------------------------------------------------

CREATE TABLE TBtipoPlato (
idTipoPlato NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY, 
nombre VARCHAR2(20) NOT NULL,
CONSTRAINT PKTBtipoPlato PRIMARY KEY(idTipoPlato)
);
-------------------------------------------------------

CREATE TABLE TBtipoIngrediente (
idTipoIngrediente NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY, 
nombre VARCHAR2(20) NOT NULL,
CONSTRAINT PKTBtipoIngrediente PRIMARY KEY(idTipoIngrediente)
);
-------------------------------------------------------

CREATE TABLE TBplato (
idPlato NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY, 
nombre VARCHAR2(80) NOT NULL,
CONSTRAINT PKTBplato PRIMARY KEY(idPlato)
);

